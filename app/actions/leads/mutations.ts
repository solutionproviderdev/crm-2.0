/**
 * Updates the project status (pipeline stage) of a lead and logs a comment.
 * Setting `status: "Ongoing"` is the convention for project-phase updates.
 *
 * @param leadId - Lead UUID
 * @param status - High-level project status (e.g., "Measuring", "Production")
 * @param subStatus - Finer-grained sub-status (e.g., "Material Ordered")
 * @param comment - Required progress note for the lead activity feed
 */
export async function updateLeadProjectStatus(
  leadId: string,
  status: string,
  subStatus: string,
  comment: string
): Promise<ActionResult<boolean>> {
  const supabase = await getUserClient();

  const { data: { user } } = await supabase.auth.getUser();

  // Fetch current project status (for diff log) + resolve "Ongoing" pipeline
  // status so the DB history trigger fires when current_status_id changes.
  const [{ data: prev }, { data: ongoingStatus }] = await Promise.all([
    supabase.from("leads").select("project_status").eq("id", leadId).single(),
    adminClient.from("pipeline_statuses").select("id, stage_id").eq("name", "Ongoing").single(),
  ]);

  const { error } = await supabase
    .from("leads")
    .update({
      status: "Ongoing",
      project_status: { status, subStatus },
      current_status_id: ongoingStatus?.id ?? null,
      current_stage_id: ongoingStatus?.stage_id ?? null,
    })
    .eq("id", leadId);

  if (error) return { success: false, error: error.message };

  await addLeadComment(leadId, comment);

  updateTag(CACHE_TAGS.LEAD_DETAILS(leadId));
  updateTag(CACHE_TAGS.LEADS);
  updateTag(CACHE_TAGS.LEAD_STATUS_COUNTS);
  revalidatePath(`/leads/${leadId}`);

  logLeadActivity({
    leadId,
    actorId: user?.id,
    action: "lead.project_status_changed",
    metadata: {
      from: prev?.project_status,
      to: { status, subStatus },
    },
  });

  return { success: true, data: true };
}

/**
 * Bulk-creates multiple lead records in a single transaction.
 *
 * @param leads - Array of lead creation inputs
 */
export async function createLeadsBulk(
  leads: CreateLeadInput[]
): Promise<ActionResult<{ inserted: number; failed: number }>> {
  if (!leads.length) return { success: false, error: "No leads to import" };

  const supabase = await getUserClient();

  const { data: { user } } = await supabase.auth.getUser();

  const { data, error } = await supabase
    .from("leads")
    .insert(leads)
    .select("id");

  if (error) return { success: false, error: error.message };

  updateTag(CACHE_TAGS.LEADS);
  updateTag(CACHE_TAGS.LEAD_STATUS_COUNTS);
  revalidatePath("/leads");

  // Log one entry per created lead (fire-and-forget)
  for (const row of data ?? []) {
    logLeadActivity({
      leadId: row.id,
      actorId: user?.id,
      action: "lead.created",
      metadata: { importedInBulk: true },
    });
  }

  return {
    success: true,
    data: {
      inserted: data?.length || 0,
      failed: leads.length - (data?.length || 0),
    },
  };
}

